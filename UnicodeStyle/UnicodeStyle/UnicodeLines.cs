namespace UnicodeStyle
{
    /// <summary>
    /// Unicode Lines char.
    /// </summary>
    public enum UnicodeLines
    {
        /// <summary>
        /// Unicode Underline Char
        /// </summary>
        Underline = '̲',

        /// <summary>
        /// Double Unicode Underline Char
        /// </summary>
        DoubleUnderline = '̳',

        /// <summary>
        /// Unicode Overline Char
        /// </summary>
        Overline = '̅',

        /// <summary>
        /// Unicode Strikethrough Char
        /// </summary>
        Strikethrough = '̶',

        /// <summary>
        /// Unicode Strikethrough Vertical Char
        /// </summary>
        StrikethroughVertical = '⃦',

        /// <summary>
        /// Unicode Slashthrough Char
        /// </summary>
        Slashthrough = '̸',

        /// <summary>
        /// Unicode Double Slashthrough Char
        /// </summary>
        DoubleSlashthrough = '⃫',
    }
}
